package Model;


public class LoaiGhe {
    String id_loaighe;
    String tenLoai;
    int phuthu;

    public String getId_loaighe() {
        return id_loaighe;
    }

    public void setId_loaighe(String id_loaighe) {
        this.id_loaighe = id_loaighe;
    }

    

    public String getTenLoai() {
        return tenLoai;
    }

    public void setTenLoai(String tenLoai) {
        this.tenLoai = tenLoai;
    }

    public int getPhuthu() {
        return phuthu;
    }

    public void setPhuthu(int phuthu) {
        this.phuthu = phuthu;
    }
    
}
